public class TarifYoneticisi {
    public static void main(String[] args){
        boolean runTime = true;
        do{
            System.out.println("Yemek tarifleri yöneticisine hoşgeldiniz. Yapmak istediğiniz işlemi seçiniz.");
            System.out.println("1- Tarif ara");
            System.out.println("2- Yeni tarif ekle");
            System.out.println("3- Kayıtlı bir tarifi düzenle");
            System.out.println("3- Kayıtlı bir tarifi sil");
        }while (runTime==true);
    }
}
